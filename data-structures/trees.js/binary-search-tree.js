// binary search tree - 2 rules
// 1. all the child nodes in the tree to the right of the root node must be greater than the current node
// and to the left is always decreasing
// 2. a node can only have up to two children
// + better than O(n) (unless unbalanced), ordered, flexible size
// - no O(1) operations

//       101
//      /   \
//    33     105
//   /\      /\
// 9   37  104 144
// (example of a balanced BST)

// Problem with unbalanced BST
//       101
//          \
//          105
//           /\
//        103 144
